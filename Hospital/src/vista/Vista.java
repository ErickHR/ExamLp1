    public String selectSexo() {
        if (rbFemenino.isSelected()) {
            return "F";
        }
        if (rbMasculino.isSelected()) {
            return "M";
        }
        return null;
    }

    public String selectSeguri() {
        if (rbSi.isSelected()) {
            return "S";
        }
        if (rbNo.isSelected()) {
            return "N";
        }
        return null;
    }

    public String seguro(String sexo) {
        if (sexo.equals("S")) {
            return "Si";
        }
        if (sexo.equals("N")) {
            return "No";
        }
        return null;
    }

    public void imprimirDatos() {

        ArrayList<String[]> listageneral = new ArrayList<>();
        int i = 1;
        for (PacienteDistrito x : PacienteDistritoDao.listar()) {

            String[] lista = new String[5];

            lista[0] = Integer.toString( i++ );
            lista[1] = x.getPaciente().getNombre();
            lista[2] = Integer.toString(x.getPaciente().getEdad());
            lista[3] = seguro(x.getPaciente().getSeguro());
            lista[4] = x.getDistrito().getNombre();

            listageneral.add(lista);

        }
        for (String[] x : listageneral) {

            model.addRow(x);
        }
        tbDatos.setModel(model);
    }

