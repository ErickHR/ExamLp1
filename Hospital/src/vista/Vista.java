    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        Paciente pac = new Paciente();
        pac.setEdad(Integer.parseInt((String) spEdad.getValue().toString()));
        pac.setNombre(txtNombre.getText());
        pac.setIddistrito(cbDistrito.getSelectedIndex() + 1);
        pac.setSeguro(selectSeguri());
        pac.setSexo(selectSexo());
        PacienteDao.registrar(pac);
    }//GEN-LAST:event_btnRegistrarActionPerformed

